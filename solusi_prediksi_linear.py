import numpy as np
from sklearn.linear_model import LinearRegression

#Database
#x = Data, y = Target
x = [[1],[3],[5],[6],[8],[9],[12],[13],[15],[18]]
y = [2, 4, 6, 9, 10, 14, 15, 18, 21, 25]

regr = LinearRegression().fit(x,y)
regr.score (x,y)

#Data uji
predict = np.array([[63]])

#Menampilkan data prediksi
print ("Prediksi")
print ("Input", predict)
print ("Output", regr.predict (predict))